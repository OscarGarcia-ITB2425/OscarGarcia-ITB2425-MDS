### OPERADORES ARITMETICOS ###

print(4+6) #--> sumar

print(4-6) #--> restar

print(4*6) #--> multiplicar

print(4/6) #--> division exacta 

print(8%3) #--> resto de la division 

print(23//6) #--> Floor division (solo da el numero entero)

print(4**7) #--> Elevacion 

print("maria" + " " + "eres" + "  "+ "una" +" " + "furcia") # Se pueden sumar varias cadenas de texto 

print("hola " * 8) #--> se puede repetir una cadena de texto "x" veces












### OPERADORES COMPARATIVOS ###

print(3<4) #--> menor que...

print(2>5)#--> major que...

print(10>= 6)#--> Menor o igual que...

print(7>=7)# --> Major o igual que...

print(5==9)#--> Igual que...

print(8!=5)#--> Diferente que...



















### OPERADORES LOGICOS ###

print(3<4 or 8!=8 ) #--> Con este operador con un solo "TRUE" ya obtenemos un true

print( 3>2 and 7==8)#--> Con este operador necesitamos que los dos booleanos sean TRUE para obtener "TRUE"

print(not 4>3)#--> Con este operador invertimos el booleano con el que interactua.