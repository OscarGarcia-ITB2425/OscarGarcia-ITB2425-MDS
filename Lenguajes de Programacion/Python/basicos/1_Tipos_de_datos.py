###TIPOS DE DATOS###

#Ahora vamos a ver los tipos basicos de informacion que tenemos.

print(type("hola mundo")) #Es un str --> Lo que significa texto normal.

print(type(5)) #Es un int --> Lo que significa que es un numero natural.

print(type(5.67)) #Es un float --> Lo que significa que es un numero real con decimales.

print(type(True)) #Es un bool --> Lo que significa que es un (Verdadero o Falso).